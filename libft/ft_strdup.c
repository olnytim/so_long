#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned char	*temp;
	size_t			i;

	temp = (unsigned char *)malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (!temp)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return ((char *)temp);
}
